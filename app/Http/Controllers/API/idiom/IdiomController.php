    //获取成语错误
    public function get_err()
    {
        $new_idioms = DB::table('rd_cyjl')
            ->orderBy('id')
            ->get();
        foreach ($new_idioms as $key=>$value) {
            $idiom = $value->idiom;
            $id = $value->id;
            $idiom_str = DB::table('rd_cyjl')
                ->where('id','>',$id+200)
                ->inRandomOrder()
                ->take(10)
                ->orderBy('id')
                ->pluck('idiom');
            $str = "";
            foreach ($idiom_str as $k1=>$v1) {
                $str .= $v1;
            }
            $arr = array_unique($this->mb_str_split($str));
            $str = implode('', $arr);
            foreach ($this->mb_str_split($idiom) as $K=>$V) {
                $pos = strpos($str, $V);
                if ($pos !== false) {
                    $str = str_replace($V,"",$str);
                }
            }
            DB::table('rd_cyjl')->where('id',$id)->update(['error_tab'=>$str]);
        }
    }
    function mb_str_split($str){
        return preg_split('/(?<!^)(?!$)/u', $str );
    }
