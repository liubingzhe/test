    //成语接龙 初始随机返回一个成语  再把接的成语返回
    public function initialize(Request $request)
    {

        $validator = Validator::make(['user_id' => $request->user_id], [
            'user_id' =>'required|integer|regex:/^[0-9]+/',
        ], [
            'regex' => ':attribute 格式不正确',
            'required' => ':attribute 不能为空',
            'integer' => ':attribute 必须是数字',

        ]);

        if ($validator->fails()) {
            return response()->json(['code' => 1002,'succ' => 'false','message' => $validator->errors()]);
        }
        //判断用户传值user_id 是否和验证用户一致
        if ($request->user_id != $this->userid) {
            return response()->json(['code' => 1017,'succ' => 'false','message' => "用户id参数有误"]);
        }
        $stage_vitality = get_config('stage_main_enter');
        $vitality = DB::table('user')->where('is_valid',1)->where('id',$request->user_id)->value('vitality');
        //判断活力值是否足够
        if ($vitality < $stage_vitality) {
            return response()->json([
                'code' => 1025,
                'succ' => 'false',
                'message' => '活力值不够',
            ]);
        } else {
            //进入游戏消耗五点活力值
            DB::table('user')->where('id',$request->user_id)->decrement('vitality', $stage_vitality);
        }


        $new_idiom = DB::table('rd_cyjl')
            ->select('idiom','error_tab','content')
            ->where('content','!=',null)
            ->where('idiom','not like','%，')
            ->inRandomOrder()
            ->take(1)
            ->get();

        $content = "";
        $error_tab = "";
        foreach ($new_idiom as $k=>$v) {
            $idiom = $v->idiom;
            $error_tab = $v->error_tab;
            $content = explode(',',$v->content);
        }
        $score = DB::table('user')->where('id',$request->user_id)->value('idiom_score');
        if ($new_idiom->isEmpty()) {
            return response([
                'code' => 1200,
                'succ' => 'true',
                'msg' => '请求成功，但无数据',
            ]);
        }
        return response([
            'code' => 1000,
            'succ' => 'true',
            'msg' => '请求成功',
            'data' => [
                'idiom' => $idiom,
                'error_tab' => $error_tab,
                'content' => $content,
                'time_len' => 10,
                'error_count' => 2,
                'idiom_score' => $score,
            ]
        ]);
    }
